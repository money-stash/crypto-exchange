const hasCyrillic = (value) => /[А-Яа-яЁё]/u.test(value);

const preserveWhitespace = (source, translatedTrimmed) => {
  const leading = source.match(/^\s*/u)?.[0] || '';
  const trailing = source.match(/\s*$/u)?.[0] || '';
  return `${leading}${translatedTrimmed}${trailing}`;
};

export const translateOperatorUiTextToEnglish = (value) => {
  if (typeof value !== 'string') return value;
  if (!hasCyrillic(value)) return value;

  const trimmed = value.trim();
  if (!trimmed) return value;

  if (EXACT_TRANSLATIONS.has(trimmed)) {
    return preserveWhitespace(value, EXACT_TRANSLATIONS.get(trimmed));
  }

  for (const [pattern, replacement] of DYNAMIC_PATTERNS) {
    if (pattern.test(trimmed)) {
      return preserveWhitespace(value, trimmed.replace(pattern, replacement));
    }
  }

  let translated = trimmed;
  for (const [ru, en] of PARTIAL_TRANSLATIONS) {
    if (translated.includes(ru)) {
      translated = translated.split(ru).join(en);
    }
  }

  return translated !== trimmed ? preserveWhitespace(value, translated) : value;
};
